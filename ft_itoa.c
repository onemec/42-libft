#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;

	if (!(str = (char *)ft_memalloc(10)))
		return (0);
	if (n == 0)
		*--str = '0';
	while (n != 0)
	{
		*--str = '0' + (((n > 0) ? n : -(long)(n)) % 10);
		if (n < 0 && n > -10)
			*--str = '-';
		n /= 10;
	}
	return (ft_strdup(str));
}
