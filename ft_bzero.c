#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*st;
	size_t	i;

	i = 0;
	st = s;
	if (n == 0)
		return ;
	else
	{
		while (i < n)
		{
			st[i] = '\0';
			i++;
		}
	}
}
